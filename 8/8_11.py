# Все 5-буквенные слова, составленные из букв А, О, У, записаны в обратном алфавитном порядке.
#  Вот начало списка:
# 1.  УУУУУ

# 2.  УУУУО

# 3.  УУУУА

# 4.  УУУОУ
# Запишите слово, которое стоит на 240-м месте от начала списка.
from itertools import product
s = 'уоа'
k = 0
for i1,i2,i3,i4,i5 in product(s,repeat=5):
    res = i1 + i2 + i3 + i4 + i5
    k+=1
    if k == 240:
        print(res)
        break